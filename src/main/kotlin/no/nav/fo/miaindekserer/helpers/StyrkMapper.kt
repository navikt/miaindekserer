private fun a(styrk: String, s: Map<String, String>): String? {
    var kode = styrk.split(".")[0]
    while (kode.length > 0) {

        val a = s[kode]
        if(a !== null) {
            return a
        }
        kode = kode.dropLast(1)
    }
    return null
}

